package mycreator.model;

public class Branch {
	private Integer branchId;
	private String branchArea;
	private String branchDistrict;
	private String branchState;
	private Integer	branchPincode;
	private String branchAddedOn;
	private String branchAddedBy;
	private String branchModifiedOn;
	private String branchmodifiedBy;
	
	
	
	public String getBranchState() {
		return branchState;
	}
	public void setBranchState(String branchState) {
		this.branchState = branchState;
	}
	public Integer getBranchId() {
		return branchId;
	}
	public void setBranchId(Integer branchId) {
		this.branchId = branchId;
	}
	public String getBranchArea() {
		return branchArea;
	}
	public void setBranchArea(String branchArea) {
		this.branchArea = branchArea;
	}
	public String getBranchDistrict() {
		return branchDistrict;
	}
	public void setBranchDistrict(String branchDistrict) {
		this.branchDistrict = branchDistrict;
	}
	public Integer getBranchPincode() {
		return branchPincode;
	}
	public void setBranchPincode(Integer branchPincode) {
		this.branchPincode = branchPincode;
	}
	public String getBranchAddedOn() {
		return branchAddedOn;
	}
	public void setBranchAddedOn(String branchAddedOn) {
		this.branchAddedOn = branchAddedOn;
	}
	public String getBranchAddedBy() {
		return branchAddedBy;
	}
	public void setBranchAddedBy(String branchAddedBy) {
		this.branchAddedBy = branchAddedBy;
	}
	public String getBranchModifiedOn() {
		return branchModifiedOn;
	}
	public void setBranchModifiedOn(String branchModifiedOn) {
		this.branchModifiedOn = branchModifiedOn;
	}
	public String getBranchmodifiedBy() {
		return branchmodifiedBy;
	}
	public void setBranchmodifiedBy(String branchmodifiedBy) {
		this.branchmodifiedBy = branchmodifiedBy;
	}
	

}
